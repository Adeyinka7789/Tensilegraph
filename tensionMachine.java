    String[] retJa(){
        String[] k = new String[6];
        try{
        FileReader fr = new FileReader("data.txt");        
        char [] a = new char[50];
        fr.read(a); // reads the content to the array
        int counter = 0;
        String charPart = "";
        for(char c : a)           
            if(c == '\n'){
                k[counter]= charPart;
                charPart = "";
                counter++;
            }
            else{
                //System.out.print(c);
                charPart = charPart+""+c;
            }
        fr.close();
        }
        catch(Exception e){
            k[0] = "";
            k[1] = "";
            k[2] = "";
            k[3] = "";
            k[4] = "";
        }
        return k;
    }
